package com.scoring.exceptions;

public class PieceJointeException extends Exception{

	public PieceJointeException() {
	}

	public PieceJointeException(String message) {
		super(message);
	}

	public PieceJointeException(Throwable cause) {
		super(cause);
	}

	public PieceJointeException(String message, Throwable cause) {
		super(message, cause);
	}

	public PieceJointeException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	
	
}
