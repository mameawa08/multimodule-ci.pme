package com.scoring.exceptions;

public class IndicateurException extends Exception{

	public IndicateurException() {
	}

	public IndicateurException(String message) {
		super(message);
	}

	public IndicateurException(Throwable cause) {
		super(cause);
	}

	public IndicateurException(String message, Throwable cause) {
		super(message, cause);
	}

	public IndicateurException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	
	
}
