package com.scoring.exceptions;

public class DirigeantException extends Exception{

	public DirigeantException() {
	}

	public DirigeantException(String message) {
		super(message);
	}

	public DirigeantException(Throwable cause) {
		super(cause);
	}

	public DirigeantException(String message, Throwable cause) {
		super(message, cause);
	}

	public DirigeantException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	
}
