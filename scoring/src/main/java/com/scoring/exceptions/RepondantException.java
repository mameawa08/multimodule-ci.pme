package com.scoring.exceptions;

public class RepondantException extends Exception{

	public RepondantException() {
	}

	public RepondantException(String message) {
		super(message);
	}

	public RepondantException(Throwable cause) {
		super(cause);
	}

	public RepondantException(String message, Throwable cause) {
		super(message, cause);
	}

	public RepondantException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}
	
	
}
